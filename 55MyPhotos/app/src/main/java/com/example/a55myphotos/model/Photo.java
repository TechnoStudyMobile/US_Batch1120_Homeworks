package com.example.a55myphotos.model;

public class Photo {

	private int albumID;
	private int id;
	private String title;
	private String url;
	private String thumbnailUrl;

	public int getAlbumID() {
		return albumID;
	}

	public int getId() {
		return id;
	}

	public String getTitle() {
		return title;
	}

	public String getUrl() {
		return url;
	}

	public String getThumbnailUrl() {
		return thumbnailUrl;
	}

}